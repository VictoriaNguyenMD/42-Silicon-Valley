int		match(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[j] != '\0')
	{
		if (s1[i] != s2[j])
		{
			if (s2[j] != '*')
				return (0);
		}
		i++;
		j++;
	}
	while (s1[i] != '\0')
		return (0);
	while (s2[j] != '\0')
	{
		if (s2[j] != '*')
			return (0);
		j++;
	}
	return (1);
}
