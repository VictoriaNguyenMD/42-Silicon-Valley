int		ft_sqrt(int nb)
{
	int mult;

	mult = 0;
	if (nb < 0)
	{
		return (0);
	}
	while (mult * mult <= nb)
	{
		if (mult * mult == nb)
			return (mult);
		mult++;
	}
	return (0);
}
