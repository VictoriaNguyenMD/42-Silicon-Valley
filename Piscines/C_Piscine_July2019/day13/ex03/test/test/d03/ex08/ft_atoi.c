int	ft_atoi(char *str)
{
	int		nbr;
	int		sign;
	int 	flag;

	nbr = 0;
	flag = 0;
	while (*str != '\0' && !(*str >= '0' && *str <= '9'))
		str++;
	str--;
	sign = 1;
	if (*str == '-')
		sign = -1;
	
	else
		str++;
	while (*str != '\0' && (*str >= '0' && *str <= '9') && flag == 0)
	{
		nbr = nbr * 10 + (*str - '0');
		str++;
	}
	return (nbr * sign);
}

