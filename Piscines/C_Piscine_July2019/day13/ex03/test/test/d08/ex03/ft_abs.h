#ifndef FT_ABS_H
# define FT_ABS_H
# define ABS(Value) ft_abs(Value)

int	ft_abs(int x)
{
	if (x == -2147483648)
		return (0);
	else
		return (x >= 0 ? x : -x);
}
#endif
